<?php

// Assignment Operators

$a  = 5;    // $a is now 5
$a += 2;    // $a is now 7
$a -= 3;    // $a is now 4
$a *= 3;    // $a is now 12
$a /= 4;    // $a is now 3
$a %= 2;    // $a is now 1
$a .= 'kg'; // $a is now "1kg"
$a += 3;    // $a is now...?

// All Bitwise operators have assignment shortcuts too!